    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        SessionFactory sf=HibernateUtil.getSessionFactory();
        Session s=sf.openSession();
        Perusahaan prs=new Perusahaan();
        Prodi pr=new Prodi();
        if(btnSimpan.getText().equals("Simpan"))
        {
            prs.setNamaPerusahaan(txtNama.getText());
            prs.setTlp(txtHP.getText());
            prs.setAlamat(txtAlamat.getText());
            Transaction tx=s.beginTransaction();
            s.saveOrUpdate(prs);
            tx.commit();
        }
        else
        {
           
            prs.setIdPerusahaan(idPrs);
            prs.setNamaPerusahaan(txtNama.getText());
            prs.setTlp(txtHP.getText());
            prs.setAlamat(txtAlamat.getText());
            Transaction tx=s.beginTransaction();
            s.saveOrUpdate(prs);
            tx.commit();
        }
        s.flush();
        s.close();
        fillTable(TMhs);
    }//GEN-LAST:event_btnSimpanActionPerformed
    
     private void Hapus(int hps) 
    {
        try
        {
            if (JOptionPane.showConfirmDialog(null,"Anda Yakin ? ","Konfirmasi" , JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION)
            {
                SessionFactory sf=HibernateUtil.getSessionFactory();
                Session s=sf.openSession();
                Transaction tx = s.beginTransaction();
                Query q = s.createQuery("Delete From Perusahaan where idPerusahaan=:idPerusahaan");
                q.setParameter("idPerusahaan", hps);
                q.executeUpdate();
                tx.commit();
                s.flush();
                s.close();
            }
        }
        catch (ClassCastException e) 
        {
            System.out.println("Err :" + e);
        }
    }
