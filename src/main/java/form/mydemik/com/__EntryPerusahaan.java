    private void fillTable(final JTable table) 
    {
        try
        {
            int count=1;
            SessionFactory sf=HibernateUtil.getSessionFactory();
            Session s=sf.openSession();
            Transaction tx = s.beginTransaction();
            Query q = s.createQuery("FROM Perusahaan");
            List resultList = q.list();
            Vector<String> tableHeaders = new Vector<String>();
            Vector tableData = new Vector();
            tableHeaders.add("No"); 
            tableHeaders.add("ID Perusahaan"); 
            tableHeaders.add("Nama Perusahaan"); 
            tableHeaders.add("Handphone");
            tableHeaders.add("Alamat");
            for(Object o : resultList) 
            {
                Perusahaan pd = (Perusahaan)o;
                Vector<Object> oneRow = new Vector<Object>();
                oneRow.add(count);
                oneRow.add(pd.getIdPerusahaan());
                oneRow.add(pd.getNamaPerusahaan());
                oneRow.add(pd.getTlp());
                oneRow.add(pd.getAlamat());
                tableData.add(oneRow);
                count++;
            }
            TMhs.setModel(new DefaultTableModel(tableData, tableHeaders));
            s.flush();
            tx.commit();
            s.close();
            nf.setLebarKolom(table);
            nf.hideColumn(table, 1);
        }
        catch (ClassCastException e) 
        {
                e.printStackTrace();
        }
    }

