    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        SessionFactory sf=HibernateUtil.getSessionFactory();
        Session s=sf.openSession();
        Prodi pr=new Prodi();
        pr.setIdProdi(idMhs);
        pr.setKodeProdi(TxtKode.getText());
        pr.setNamaProdi(TxtNama.getText());
        Transaction tx=s.beginTransaction();
        s.saveOrUpdate(pr);
        tx.commit();
        s.flush();
        s.close();
        fillTable(TProdi);
    }//GEN-LAST:event_btnSimpanActionPerformed
    private void Hapus(int hps) 
    {
        try
        {
            if (JOptionPane.showConfirmDialog(null,"Anda Yakin ? ","Konfirmasi" , JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION)
            {
                SessionFactory sf=HibernateUtil.getSessionFactory();
                Session s=sf.openSession();
                Transaction tx = s.beginTransaction();
                Query q = s.createQuery("Delete From Prodi where idProdi=:idProdi");
                q.setParameter("idProdi", hps);
                q.executeUpdate();
                tx.commit();
                s.flush();
                s.close();
            }
        }
        catch (ClassCastException e) 
        {
            System.out.println("Err :" + e);
        }
    }
